void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	unsigned int	n;

	if (nb < 0)
	{
		ft_putchar('-');
		n = -1 * nb;
	}
	else
	{
		n = nb;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
	}
	ft_putchar((char)(n % 10 + '0'));
}
